var slide= 0;
function plusSlides(i){
  var hello = $('.slideshow-container').children('.mySlides')
  total = hello.length-1;
  if(i == -1){
    if(slide == 0){
      hello[total].style.display = "";
      hello[slide].style.display = "none";
      slide = total;
    }else{
      hello[slide].style.display = "none";
      hello[slide-1].style.display = "";
      slide = slide -1;
    }
  }else{
    if(slide == total){
      hello[0].style.display = "";
      hello[slide].style.display = "none";
      slide = 0;
    }else{
      hello[slide].style.display = "none";
      hello[slide+1].style.display = "";
      slide = slide +1;
    }
  }

}

